# Função de densidade da kw ----------------------------------------------------

pdf_kw <- function(par,x){
  alpha = par[1]
  beta1 = par[2]
  f = alpha*beta1*x^(alpha-1)*(1-x^alpha)^(beta1-1)
  f
}

# Log-verossimilhança da kW ----------------------------------------------------

mlog_kw <- function(par,x){
  s <- sum(log(pdf_kw(par=par,x=x)))
  s
}

